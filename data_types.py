# Strings - used for storing text data like names, messages, or labels
name = "Bilal"

# Integers - used for whole numbers like counts, ages, or IDs
age = 28

# Floats - used for decimal numbers like prices, measurements, or percentages
price = 19.99

# Booleans - used for true/false values like flags or conditions
is_active = True

# Lists - used for ordered, changeable collections of items
skills = ["Python", "Git", "AI tools"]

# Dictionaries - used for storing key-value pairs like structured data or records
profile = {"name": "Bilal", "role": "AI-Native Developer", "age": 28}

print(name)
print(age)
print(price)
print(is_active)
print(skills)
print(profile)
